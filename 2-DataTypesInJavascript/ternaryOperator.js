/*
Ternary Operator
Condition ? Expresion 1 : Expresion 2
if Condition is true so  return Expresion 1
if Condition is false so return Expresion 2
*/
let result = 3 > 2 ? "Is True" : "Is False";
console.log(result);

let num = 5;
let evenNumber = num % 2 == 0 ? "Is even" : "Is odd";
console.log(evenNumber);
